//WAP using buffer reader class and take the input from the user for displaying name and age of a student; 


import java.io.*;
class Main{
    public static void main(String args[]){
        try{
            BufferedReader reader  = new BufferedReader(new InputStreamReader(System.in));
            String name  = reader.readLine();
            int age = Integer.parseInt(reader.readLine());
            System.out.println("Name is: "+name+" \nAge is: "+age); //Ram,20
        }catch(Exception e){
            System.out.println("Error occured..." + e.getMessage());
        }
    }
}

// Expected Output: 
// Name is: Ram
// Age is: 20
